#include "funciones.h"

bool esNegativo(int numero){
    if(numero<0){
        return true;
    }
    return false;
}

bool esCero(int numero){
    if(numero==0){
        return true;
    }
    return false;
}

bool esPositivo(int numero){
    if(numero>0){
        return true;
    }
    return false;
}